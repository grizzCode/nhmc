<div class="about-wrapper">
  <p>
    New Horizons Maritime Center (NHMC) was formed exclusively for the charitable purposes of providing a dynamic hands-on, apprentice-based
    learning environment, designed specifically for youth development and community betterment. These programs have been developed from
    a concern for at-risk and troubled youth who struggle to succeed in the traditional educational system and who often drop out of school
    before graduating from high school.
  </p>
  <p>
    NHMC will offer after-school programs, educational workshops, apprentice possibilities, and a safe place to pursue productive and useful
    life changing educational activities. Through the restoration of donated boats needing repair, and under the careful supervision of NHMC
    staff and volunteers, youth groups and other interested people in the community will learn a wide variety of trade and social skills
    necessary for success in today’s competitive world. Participants will gain a greater sense of tradition, respect, and societal values
    as they tap into the rich maritime traditions our country has to offer. New interests will be developed and innate potential realized.
  </p>
  <p>
    Programs will begin in the Spring of 2013 with the restoration of a fleet of Star sail boats, as well as scratch building of the Bella 10
    rowing boat. Using the Star and Bella boats as a learning/project platform, the focus of NHMC will be to teach trade skills to youth.
    The restoration / building projects will provide opportunities for scholarly research as well as all of the various facets of yacht restoration
    including reading lofting plans, collecting research data, photo documentation of the restoration progress, welding, woodworking, painting,
    marine electrical systems, navigation instrumentation, and safety requirements. These trade skills will require the practical application of math,
    physics, science, language comprehension, problem solving and technical drawing. Self-esteem and feelings of success will be natural by-products
    as benchmarks are completed. Not only will the youth involved feel the impact of this project but it will also be felt by community leaders,
    correctional institutions, tradesmen who serve as mentors, friends and family of the youth as well as the extensive worldwide sailing community.
    These life altering stories will be documented, filmed and written about for future generations.
  </p>
  <p>
    Upon obtaining 501(c)3 status we will institute the following 5 phases of development. Interwoven throughout each phase will be an emphasis on
    research and documentation of participants.
  </p>
